--
-- 9. Helpers.
--

-- Normalize a phone number for matching: keep digits only, then return the
-- last 9 digits (covers FR mobiles whether stored as +33XXXXXXXXX, 0XXXXXXXXX,
-- or 33XXXXXXXXX). Returns NULL when input is too short or null.
create or replace function public.allo_normalize_phone(p text)
returns text
language sql
immutable
set search_path to ''
as $$
  select case
    when p is null then null
    when length(regexp_replace(p, '\D', '', 'g')) < 9 then null
    else right(regexp_replace(p, '\D', '', 'g'), 9)
  end
$$;

grant all on function public.allo_normalize_phone(text) to authenticated;
grant all on function public.allo_normalize_phone(text) to service_role;

--
-- 10. process_allo_call(payload jsonb) → jsonb
--
-- Atomic matcher + writer for an Allo `call.completed` event.
-- Idempotent on `allo_call_id`: replaying the same payload returns the
-- existing call_logs row without creating duplicates.
--
-- Expected payload shape (lenient — extra fields are kept in raw_payload):
--   {
--     "event": "call.completed",
--     "data": {
--       "call_id":          "<unique allo id>",
--       "direction":        "inbound" | "outbound",
--       "from":             "+33...",
--       "to":               "+33...",
--       "line_phone":       "+33...",       -- optional, falls back to from/to
--       "started_at":       "ISO 8601",
--       "ended_at":         "ISO 8601",
--       "duration_seconds": 120,
--       "status":           "answered" | "missed" | ...,
--       "recording_url":    "https://...",
--       "ai_summary":       "...",
--       "transcript":       "...",
--       "contact_id":       "<allo contact id>"  -- optional
--     }
--   }
--
create or replace function public.process_allo_call(p_payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path to ''
as $$
declare
  v_data            jsonb := coalesce(p_payload -> 'data', p_payload);
  v_call_id         text  := v_data ->> 'call_id';
  v_direction       text  := lower(coalesce(v_data ->> 'direction', 'inbound'));
  v_from            text  := v_data ->> 'from';
  v_to              text  := v_data ->> 'to';
  v_line_phone      text  := v_data ->> 'line_phone';
  v_allo_contact_id text  := v_data ->> 'contact_id';
  v_external_phone  text;
  v_external_norm   text;
  v_sales_id        bigint;
  v_contact_id      bigint;
  v_deal_id         bigint;
  v_lead_tag_id     bigint;
  v_existing        public.call_logs%rowtype;
  v_inserted_id     bigint;
  v_was_inserted    boolean := false;
  v_contact_created boolean := false;
begin
  if v_call_id is null or v_call_id = '' then
    raise exception 'process_allo_call: payload missing data.call_id';
  end if;

  -- Fast path: idempotent re-delivery. Return the existing row untouched.
  select * into v_existing from public.call_logs where allo_call_id = v_call_id;
  if found then
    return jsonb_build_object(
      'call_log_id',  v_existing.id,
      'contact_id',   v_existing.contact_id,
      'sales_id',     v_existing.sales_id,
      'deal_id',      v_existing.deal_id,
      'inserted',     false,
      'contact_created', false
    );
  end if;

  -- Resolve which Allo line was used. Falls back to the local side of the
  -- call when line_phone is not supplied (outbound: from = line; inbound: to = line).
  if v_line_phone is null or v_line_phone = '' then
    v_line_phone := case when v_direction = 'outbound' then v_from else v_to end;
  end if;

  -- Map line → sales_id via allo_line_owners (left join on normalized form).
  select lo.sales_id into v_sales_id
    from public.allo_line_owners lo
   where public.allo_normalize_phone(lo.allo_phone_number)
       = public.allo_normalize_phone(v_line_phone)
   limit 1;

  -- The "external" phone is the counterparty (the contact's number).
  v_external_phone := case when v_direction = 'outbound' then v_to else v_from end;
  v_external_norm  := public.allo_normalize_phone(v_external_phone);

  -- Try to match an existing CRM contact:
  --   a) via the pivot if Allo gave us a contact id we've seen before
  --   b) otherwise by phone number, normalized
  if v_allo_contact_id is not null and v_allo_contact_id <> '' then
    select acl.contact_id into v_contact_id
      from public.allo_contact_links acl
     where acl.allo_contact_id = v_allo_contact_id
     limit 1;
  end if;

  if v_contact_id is null and v_external_norm is not null then
    select c.id into v_contact_id
      from public.contacts c
      cross join lateral jsonb_array_elements(coalesce(c.phone_jsonb, '[]'::jsonb)) as phones(phone_obj)
     where public.allo_normalize_phone(phones.phone_obj ->> 'number') = v_external_norm
     order by c.id
     limit 1;
  end if;

  -- No match → auto-create a contact tagged 'lead-from-allo'.
  if v_contact_id is null then
    select id into v_lead_tag_id from public.tags where name = 'lead-from-allo' limit 1;

    insert into public.contacts (
      first_name,
      last_name,
      sales_id,
      phone_jsonb,
      tags,
      first_seen,
      last_seen,
      _sync_origin
    ) values (
      'Allo',
      coalesce(v_external_phone, 'Unknown'),
      v_sales_id,
      case
        when v_external_phone is null then '[]'::jsonb
        else jsonb_build_array(jsonb_build_object('number', v_external_phone, 'type', 'Mobile'))
      end,
      case when v_lead_tag_id is null then '{}'::bigint[] else array[v_lead_tag_id] end,
      coalesce((v_data ->> 'started_at')::timestamptz, now()),
      coalesce((v_data ->> 'ended_at')::timestamptz, (v_data ->> 'started_at')::timestamptz, now()),
      'allo'
    )
    returning id into v_contact_id;

    v_contact_created := true;
  end if;

  -- Persist the Allo↔CRM link if we have an Allo contact id and it isn't
  -- already recorded (composite PK + on conflict do nothing keeps it idempotent).
  if v_allo_contact_id is not null and v_allo_contact_id <> '' then
    insert into public.allo_contact_links (contact_id, allo_contact_id)
      values (v_contact_id, v_allo_contact_id)
      on conflict do nothing;
  end if;

  -- Attach to the contact's currently-open deal if there is exactly one
  -- candidate (archived_at IS NULL = pipeline still active). Most-recent wins
  -- on ties so the newest deal benefits from the activity log.
  select d.id into v_deal_id
    from public.deals d
   where d.archived_at is null
     and d.contact_ids @> array[v_contact_id]
   order by d.updated_at desc, d.id desc
   limit 1;

  -- Insert the call log. on conflict guards against the (very rare) race
  -- where two concurrent webhook deliveries reach this point at once.
  insert into public.call_logs (
    allo_call_id,
    direction,
    from_number,
    to_number,
    line_phone,
    status,
    duration_seconds,
    started_at,
    ended_at,
    recording_url,
    ai_summary,
    transcript,
    contact_id,
    sales_id,
    deal_id,
    raw_payload
  ) values (
    v_call_id,
    case when v_direction in ('inbound', 'outbound') then v_direction else 'inbound' end,
    v_from,
    v_to,
    v_line_phone,
    v_data ->> 'status',
    nullif(v_data ->> 'duration_seconds', '')::integer,
    nullif(v_data ->> 'started_at', '')::timestamptz,
    nullif(v_data ->> 'ended_at', '')::timestamptz,
    v_data ->> 'recording_url',
    v_data ->> 'ai_summary',
    v_data ->> 'transcript',
    v_contact_id,
    v_sales_id,
    v_deal_id,
    p_payload
  )
  on conflict (allo_call_id) do nothing
  returning id into v_inserted_id;

  v_was_inserted := v_inserted_id is not null;

  -- If the conflict path won, fetch the existing row's id so callers always
  -- see something useful in the response.
  if v_inserted_id is null then
    select id into v_inserted_id from public.call_logs where allo_call_id = v_call_id;
  end if;

  return jsonb_build_object(
    'call_log_id',     v_inserted_id,
    'contact_id',      v_contact_id,
    'sales_id',        v_sales_id,
    'deal_id',         v_deal_id,
    'inserted',        v_was_inserted,
    'contact_created', v_contact_created
  );
end;
$$;

grant all on function public.process_allo_call(jsonb) to service_role;
-- authenticated keeps it reachable from the dashboard SQL editor for ops.
grant all on function public.process_allo_call(jsonb) to authenticated;

