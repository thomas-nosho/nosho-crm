-- Allo ↔ Nosho-CRM sync — Phase 1 (calls only).
--
-- Foundations for the Allo integration:
--   • call_logs       — one row per Allo call.completed event
--   • sms_messages    — created now so phase 2 can land without another migration
--   • allo_contact_links — pivot CRM contact ↔ Allo contact id
--   • allo_line_owners   — Allo phone number → sales_id mapping
--   • contacts._sync_origin — anti-loop marker for the (future) reverse sync
--   • lead-from-allo tag    — applied to contacts auto-created by the webhook
--   • process_allo_call(jsonb) — idempotent matcher+writer used by n8n
--
-- All writes go through process_allo_call() so the webhook stays a thin
-- transport: validate signature in n8n, then forward the payload to Postgres.

--
-- 1. Marker column on contacts (used by phase 3 to break feedback loops).
--
alter table public.contacts
  add column if not exists _sync_origin text;

comment on column public.contacts._sync_origin is
  'Identifier of the system that last wrote this row via an external sync (e.g. ''allo''). NULL = local CRM edit. Used by reverse-sync workflows to skip echoes.';

--
-- 2. Allo line ↔ sales mapping (seeded manually after migration runs).
--
create table if not exists public.allo_line_owners (
    id bigint generated by default as identity primary key,
    allo_phone_number text not null unique,
    sales_id bigint not null references public.sales(id) on delete cascade,
    label text,
    created_at timestamp with time zone not null default now()
);

create index if not exists idx_allo_line_owners_sales on public.allo_line_owners(sales_id);

--
-- 3. Pivot mapping CRM contact ↔ Allo contact id.
--    Composite PK so the same CRM contact can be linked to multiple Allo
--    contact records over time (rare, but cheaper than a one-to-one).
--
create table if not exists public.allo_contact_links (
    contact_id bigint not null references public.contacts(id) on delete cascade,
    allo_contact_id text not null,
    created_at timestamp with time zone not null default now(),
    primary key (contact_id, allo_contact_id)
);

create unique index if not exists uq_allo_contact_links_allo_id
    on public.allo_contact_links(allo_contact_id);

--
-- 4. Call logs.
--
create table if not exists public.call_logs (
    id bigint generated by default as identity primary key,
    allo_call_id text not null unique,
    direction text not null check (direction in ('inbound', 'outbound')),
    from_number text,
    to_number text,
    line_phone text,
    status text,
    duration_seconds integer,
    started_at timestamp with time zone,
    ended_at timestamp with time zone,
    recording_url text,
    ai_summary text,
    transcript text,
    contact_id bigint references public.contacts(id) on delete set null,
    sales_id bigint references public.sales(id) on delete set null,
    deal_id bigint references public.deals(id) on delete set null,
    raw_payload jsonb not null,
    created_at timestamp with time zone not null default now()
);

create index if not exists idx_call_logs_contact on public.call_logs(contact_id, started_at desc);
create index if not exists idx_call_logs_sales on public.call_logs(sales_id, started_at desc);
create index if not exists idx_call_logs_deal on public.call_logs(deal_id, started_at desc);
create index if not exists idx_call_logs_started_at on public.call_logs(started_at desc);

--
-- 5. SMS messages (table only — phase 2 will add process_allo_sms()).
--
create table if not exists public.sms_messages (
    id bigint generated by default as identity primary key,
    allo_message_id text not null unique,
    direction text not null check (direction in ('inbound', 'outbound')),
    from_number text,
    to_number text,
    line_phone text,
    body text,
    sent_at timestamp with time zone,
    contact_id bigint references public.contacts(id) on delete set null,
    sales_id bigint references public.sales(id) on delete set null,
    raw_payload jsonb not null,
    created_at timestamp with time zone not null default now()
);

create index if not exists idx_sms_messages_contact on public.sms_messages(contact_id, sent_at desc);
create index if not exists idx_sms_messages_sales on public.sms_messages(sales_id, sent_at desc);

